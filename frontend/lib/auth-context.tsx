'use client'

import React, { createContext, useContext, useState, useCallback, useEffect } from 'react'

export interface User {
  id: number
  email: string
  full_name: string
  role: 'candidate' | 'hr'
  is_active: boolean
  created_at: string
}

interface AuthContextType {
  user: User | null
  token: string | null
  isLoading: boolean
  isAuthenticated: boolean
  register: (email: string, password: string, full_name: string, role: 'candidate' | 'hr') => Promise<void>
  login: (email: string, password: string) => Promise<void>
  logout: () => void
  refreshUser: () => Promise<void>
}

const AuthContext = createContext<AuthContextType | undefined>(undefined)

export function AuthProvider({ children }: { children: React.ReactNode }) {
  const [user, setUser] = useState<User | null>(null)
  const [token, setToken] = useState<string | null>(null)
  const [isLoading, setIsLoading] = useState(true)

  // Load token from localStorage on mount
  useEffect(() => {
    const savedToken = localStorage.getItem('auth_token')
    if (savedToken) {
      setToken(savedToken)
      // Verify token by fetching current user
      fetchCurrentUser(savedToken)
    } else {
      setIsLoading(false)
    }
  }, [])

  const envUrl = process.env.NEXT_PUBLIC_API_BASE_URL
  const API_BASE_URL = (envUrl && envUrl !== 'undefined') ? envUrl : 'http://localhost:8000'
  console.log("Current API_BASE_URL:", API_BASE_URL)


  const fetchCurrentUser = useCallback(async (authToken: string) => {
    try {
      const response = await fetch(`${API_BASE_URL}/api/auth/me`, {
        headers: {
          'Authorization': `Bearer ${authToken}`,
          'Content-Type': 'application/json'
        }
      })

      if (response.ok) {
        const userData = await response.json()
        setUser(userData)
        setToken(authToken)
      } else {
        // Token invalid, clear it
        localStorage.removeItem('auth_token')
        setToken(null)
        setUser(null)
        if (response.status === 401) {
          window.location.href = '/auth/login?expired=true'
        }
      }
    } catch (error) {
      console.error('Error fetching current user:', error)
      localStorage.removeItem('auth_token')
      setToken(null)
      setUser(null)
    } finally {
      setIsLoading(false)
    }
  }, [])

  const register = useCallback(async (email: string, password: string, full_name: string, role: 'candidate' | 'hr') => {
    setIsLoading(true)
    try {
      console.log("[v0] Registering user:", { email, full_name, role })

      const response = await fetch(`${API_BASE_URL}/api/auth/register`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ email, password, full_name, role }),
        credentials: 'include'
      })

      console.log("[v0] Register response status:", response.status)

      if (!response.ok) {
        let errorMessage = 'Registration failed'
        try {
          const error = await response.json()
          errorMessage = error.detail || error.message || errorMessage
        } catch (e) {
          errorMessage = `Server error (${response.status}): ${response.statusText}`
        }
        throw new Error(errorMessage)
      }

      const userData = await response.json()
      console.log("[v0] User registered successfully:", userData)
      setUser(userData)
    } catch (error) {
      // Don't log expected registration errors (email exists, etc.) to console
      if (error instanceof TypeError && error.message.includes('fetch')) {
        console.error("[v0] Registration error:", error)
        throw new Error(`Unable to connect to backend server at ${API_BASE_URL}. Please ensure the FastAPI server is running.`)
      }
      throw error
    } finally {
      setIsLoading(false)
    }
  }, [])

  const login = useCallback(async (email: string, password: string) => {
    setIsLoading(true)
    try {
      console.log("[v0] Logging in user:", { email })

      const response = await fetch(`${API_BASE_URL}/api/auth/login`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ email, password }),
        credentials: 'include'
      })

      console.log("[v0] Login response status:", response.status)

      if (!response.ok) {
        let errorMessage = 'Login failed'
        try {
          const error = await response.json()
          errorMessage = error.detail || error.message || errorMessage
        } catch (e) {
          errorMessage = `Server error (${response.status}): ${response.statusText}`
        }
        throw new Error(errorMessage)
      }

      const data = await response.json()
      const authToken = data.access_token

      // Save token and fetch user
      localStorage.setItem('auth_token', authToken)
      setToken(authToken)

      // Fetch user info
      await fetchCurrentUser(authToken)
    } catch (error) {
      // Don't log expected authentication errors to console (wrong password, etc.)
      // as they're handled by the UI and would trigger Next.js error overlay
      if (error instanceof TypeError && error.message.includes('fetch')) {
        console.error("[v0] Login error:", error)
        throw new Error(`Unable to connect to backend server at ${API_BASE_URL}. Please ensure the FastAPI server is running.`)
      }
      throw error
    } finally {
      setIsLoading(false)
    }
  }, [fetchCurrentUser])

  const logout = useCallback(() => {
    setUser(null)
    setToken(null)
    localStorage.removeItem('auth_token')
  }, [])

  const refreshUser = useCallback(async () => {
    if (token) {
      await fetchCurrentUser(token)
    }
  }, [token, fetchCurrentUser])

  const value: AuthContextType = {
    user,
    token,
    isLoading,
    isAuthenticated: !!user && !!token,
    register,
    login,
    logout,
    refreshUser
  }

  return (
    <AuthContext.Provider value={value}>
      {children}
    </AuthContext.Provider>
  )
}

export function useAuth() {
  const context = useContext(AuthContext)
  if (context === undefined) {
    throw new Error('useAuth must be used within AuthProvider')
  }
  return context
}
